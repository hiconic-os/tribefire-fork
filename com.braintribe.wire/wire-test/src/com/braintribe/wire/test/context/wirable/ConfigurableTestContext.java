package com.braintribe.wire.test.context.wirable;

public class ConfigurableTestContext implements TestContext {
	private String name;
	private boolean important;
	private String id;
	
	public ConfigurableTestContext(String name, boolean important, String id) {
		super();
		this.name = name;
		this.important = important;
		this.id = id;
	}

	@Override
	public String name() {
		return name;
	}

	@Override
	public boolean important() {
		return important;
	}

	@Override
	public String id() {
		return id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ConfigurableTestContext other = (ConfigurableTestContext) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
	
	
}
